        #region contentNodeRoutes

        /// <summary>
        /// Gets all content node routes.
        /// </summary>
        /// <returns>IList&lt;ContentNodeRoute&gt;.</returns>
        public IList<ContentNodeRoute> GetAllContentNodeRoutes()
        {
            return _unitOfWork.ContentNodeRouteRepository.GetAll();
        }

        /// <summary>
        /// Searches the content node routes.
        /// </summary>
        /// <param name="where">The where.</param>
        /// <param name="sort">The sort.</param>
        /// <param name="page">The page.</param>
        /// <param name="pageSize">Size of the page.</param>
        /// <returns>IPagedList&lt;ContentNodeRoute&gt;.</returns>
        public IPagedList<ContentNodeRoute> SearchContentNodeRoutes(string where, string sort, int page, int pageSize = 20)
        {
            string sortExpression = sort;
            if (string.IsNullOrEmpty(sortExpression) || sortExpression.ToLower() == "default")
            {
                var entityHelper = new EntityHelper<ContentNodeRoute>();
                sortExpression = entityHelper.GetDefaultSortExpression();
            }
            else
            {
                sortExpression = sortExpression.Replace("-", " ");
            }

            return _unitOfWork.ContentNodeRouteRepository.GetPagedList(page, pageSize, ParseJSONSearchString<ContentNodeRoute>(where), sortExpression, a => a.ContentNode);
        }

        /// <summary>
        /// Gets the content node route by identifier.
        /// </summary>
        /// <param name="id">The identifier.</param>
        /// <returns>ContentNodeRoute.</returns>
        public ContentNodeRoute GetContentNodeRouteById(int id)
        {
            return _unitOfWork.ContentNodeRouteRepository.GetSingle(a => a.Id == id, a => a.ContentNode);
        }

        /// <summary>
        /// Adds the content node route.
        /// </summary>
        /// <param name="contentNodeRoutes">The content node routes.</param>
        public void AddContentNodeRoute(params ContentNodeRoute[] contentNodeRoutes)
        {
            _unitOfWork.ContentNodeRouteRepository.Add(contentNodeRoutes);
            _unitOfWork.Save();
        }

        /// <summary>
        /// Updates the content node route.
        /// </summary>
        /// <param name="contentNodeRoutes">The content node routes.</param>
        public void UpdateContentNodeRoute(params ContentNodeRoute[] contentNodeRoutes)
        {
            _unitOfWork.ContentNodeRouteRepository.Update(contentNodeRoutes);
            _unitOfWork.Save();
        }

        /// <summary>
        /// Removes the content node route.
        /// </summary>
        /// <param name="contentNodeRoutes">The content node routes.</param>
        public void RemoveContentNodeRoute(params ContentNodeRoute[] contentNodeRoutes)
        {
            _unitOfWork.ContentNodeRouteRepository.Remove(contentNodeRoutes);
            _unitOfWork.Save();
        }

        #endregion
    }
