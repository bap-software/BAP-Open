namespace BAP.Common
{

    /// <summary>
    /// Interface is assigned to the BAP entities which need to support full text search functionality.
    /// </summary>
    public interface IFullTextSearchable
    {
        /// <summary>
        /// Function to return WHERE expression to search for an entity using part of the text specified.
        /// </summary>
        /// <param name="searchValue">Text to search for</param>
        /// <returns>Linq2Sql expression text to search for a given part of text.</returns>
        string FullTextSearchExpression(string searchValue);
    }
}
